function techWithVersion(tech: DetectedTech, pkg: PkgJson | null): string {
  const name = DISPLAY_NAME[tech];
  const packageName = TECH_TO_PACKAGE[tech];
  if (!packageName || !pkg) return name;
  const deps = { ...pkg.dependencies, ...pkg.devDependencies };
  const raw = deps[packageName];
  if (!raw) return name;
  return `${name} ${stripSemver(raw)}`;
}

function buildStackSection(detected: DetectedTech[], pkg: PkgJson | null): string {
  if (detected.length === 0) return '(no stack detected)';
  return detected.map(t => `- ${techWithVersion(t, pkg)}`).join('\n');
}

// Script names extracted into the Commands section. Prefix match via
// `db:` and `test:` handled separately. Case-insensitive.
const KNOWN_SCRIPT_NAMES = new Set([
  'dev', 'start', 'build',
  'test',
  'lint', 'format',
  'typecheck', 'type-check', 'tsc',
  'migrate', 'seed',
]);

function isKnownScript(name: string): boolean {
  const lower = name.toLowerCase();
  if (KNOWN_SCRIPT_NAMES.has(lower)) return true;
  if (lower.startsWith('test:')) return true;
  if (lower.startsWith('db:')) return true;
  return false;
}

function buildCommandsSection(scripts: Record<string, string>, runPrefix: string): string {
  const entries = Object.entries(scripts).filter(([k]) => isKnownScript(k));
  if (entries.length === 0) return '(no recognized package.json scripts. Fill in the commands agents should run for build, test, lint, etc.)';
  return entries.map(([k, v]) => `- \`${runPrefix} ${k}\`: \`${v}\``).join('\n');
}

function buildSummarySection(techs: DetectedTech[], key: 'conventions' | 'avoid', detection: DetectionResult): string {
  const bullets: string[] = [];
  for (const t of techs) {
    const entry = AGENTS_SUMMARY[t];
    if (!entry) continue;
    for (const line of entry[key]) bullets.push(`- ${line}`);
    // Dynamic: surface detected Python tool in fastapi's conventions.
    if (t === 'fastapi' && key === 'conventions' && detection.pythonTool) {
      bullets.push(`- ${PYTHON_TOOL_BULLETS[detection.pythonTool]}`);
    }
  }
  return bullets.length > 0 ? bullets.join('\n') : '(no stack-specific guidance)';
}

